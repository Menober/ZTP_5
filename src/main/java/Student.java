public class Student {
    int indeks;
    String plec;
    int wiek;

    public Student(int indeks, String plec,int wiek){
        this.indeks=indeks;
        this.plec=plec;
        this.wiek=wiek;
    }

    public int getIndeks() {
        return indeks;
    }

    public String getPlec() {
        return plec;
    }

    public int getWiek() {
        return wiek;
    }
}
